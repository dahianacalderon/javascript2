let numero = 7;

switch (true) {
  case (numero > 0):
    console.log("Número positivo.");
    break;
  case (numero < 0):
    console.log("Número negativo.");
    break;
  case (numero === 0):
    console.log("El número es cero.");
    break;
  default:
    console.log("Número no clasificado.");
}


